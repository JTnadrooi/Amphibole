//laad de image en als die is geladen blijft hij updaten
function loadhandler() {
    update();
}





//zorgt ervoor dan window animatie blijft updaten en cover de rest van de code
function update() {
    //dit kijkt naar de sprite en voorspelt waar hij is
    window.requestAnimationFrame(update, canvas);
    var futureX = amphiboleFull.x + xspeed;
    var futureY = amphiboleFull.y + yspeed;
//dit kijkt voor horizontale movment of hij tegen een collison tile  gaat enzo niet dat update hij
 if (!isSolidTileAtXY(futureX, amphiboleFull.y) && 
 !isSolidTileAtXY(futureX + amphiboleFull.width, amphiboleFull.y) && 
 !isSolidTileAtXY(futureX, amphiboleFull.y + amphiboleFull.height) && 
 !isSolidTileAtXY(futureX + amphiboleFull.width, amphiboleFull.y + amphiboleFull.height)) {
 amphiboleFull.x = futureX; 
}

//hetzelfda maar dan vertical
if (!isSolidTileAtXY(amphiboleFull.x, futureY) && 
 !isSolidTileAtXY(amphiboleFull.x + amphiboleFull.width, futureY) && 
 !isSolidTileAtXY(amphiboleFull.x, futureY + amphiboleFull.height) && 
 !isSolidTileAtXY(amphiboleFull.x + amphiboleFull.width, futureY + amphiboleFull.height)) {
 amphiboleFull.y = futureY; 
}
//dit is de movement de ! is als die niet is ingerukt
if (moveUp && !moveDown) {
        yspeed = -3;
    }
    if (moveDown && !moveUp) {
        yspeed= 3;
    }
    if (moveLeft && !moveRight) {
        xspeed = -3;
    }
    if (moveRight && !moveLeft) {
        xspeed = 3;
    }
    if (!moveUp && !moveDown) {
        yspeed = 0;
    }
    if (!moveLeft && !moveRight) {
        xspeed = 0;
    }
    //kijkt of het kleiner is dan nul en als dat zo is zou die moeten stoppen maar ik een met de tilemap een soor van border gemaakt dus dit booit niet echt op dit moment
    if (amphiboleFull.x < 0) {
        xspeed = 0;
    }
    if (amphiboleFull.y < 0) {
        yspeed = 0;
    }
    if (amphiboleFull.x + amphiboleFull.width > canvas.width) {
        amphiboleFull.x = canvas.width - amphiboleFull.width;
    }
    if (amphiboleFull.y + amphiboleFull.height > canvas.height) {
        amphiboleFull.y = canvas.height - amphiboleFull.height;
    }
    render();

}
//dit teken de spirte op een nieuwe positie en cleared de canvas elke keer als die op de nieuwe positie komt
function render() {
    drawingSurface.clearRect(0, 0, canvas.width, canvas.height);
    drawingSurface.drawImage(
        image,
        Math.floor(amphiboleFull.x), Math.floor(amphiboleFull.y),
        amphiboleFull.width, amphiboleFull.height

    );
}



var tileSize = 32;
//dit kijkt naar de tilemap van col(x) en row(y) en als ze daar zitten dan kijkt hij of het een 1 is als het geen 1 is word het false
function isSolidTileAtXY(x,y){
var col = Math.floor(x / tileSize);
var row = Math.floor(y / tileSize);

if (row >= 0 && row < tileMap.length  && col >0 && col <tileMap[0].length ) {
    return tileMap[row][col] ===1;
}
    return false;
}

